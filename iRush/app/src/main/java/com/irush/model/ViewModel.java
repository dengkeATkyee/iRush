public class ViewModel {
    int layout;
    int[] imgs;
    int[] texts;

    public int getLayout() {
        return layout;
    }

    public void setLayout(int layout) {
        this.layout = layout;
    }

    public int[] getImgs() {
        return imgs;
    }

    public void setImgs(int[] imgs) {
        this.imgs = imgs;
    }

    public int[] getTexts() {
        return texts;
    }

    public void setTexts(int[] texts) {
        this.texts = texts;
    }
}
